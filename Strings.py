#Strings
#String slicing

mystr="My name is Nishant Raj"
print(mystr)
print(len(mystr))
print(mystr[4])
print(mystr[3:24])
print(mystr[:])
print(mystr[3:])
print(mystr[:16])
print(mystr[2:20:3])

#Negative string
MYSTRING="I am a student of LPU"
print(MYSTRING)
print(len(MYSTRING))
print(MYSTRING[-5:-1])
print(MYSTRING[13:-5])
print(MYSTRING[:-4])
print(MYSTRING[-13:])
print(MYSTRING[-15:-2:2])

